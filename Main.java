public class Main {
    public static void main(String[] args) {
        new A(100); // Создание объекта класса A
    }
}

class A {
    {
        // Логический блок 1 - выполняется ПЕРВЫМ из блоков при создании объекта
        // Поле id еще не инициализировано, поэтому имеет значение по умолчанию 0
        System.out.println("logic (1) id= " + this.id); // Вывод: logic (1) id= 0
    }

    static {
        // Статический блок - выполняется ПЕРВЫМ при загрузке класса в память
        // Выполняется только один раз при первом обращении к классу
        System.out.println("static logic"); // Вывод: static logic
    }

    private int id = 1; // Инициализация поля - выполняется ПОСЛЕ логических блоков

    public A(int id) {
        // Конструктор - выполняется ПОСЛЕ всех логических блоков и инициализации полей
        this.id = id; // Присваивание нового значения параметра
        System.out.println("ctor id= " + id); // Вывод: ctor id= 100
    }

    {
        // Логический блок 2 - выполняется ВТОРЫМ из блоков при создании объекта
        // Поле id уже инициализировано значением 1
        System.out.println("logic (2) id= " + id); // Вывод: logic (2) id= 1
    }
}

/*
ОЖИДАЕМЫЙ ВЫВОД ПРОГРАММЫ:
static logic
logic (1) id= 0
logic (2) id= 1
ctor id= 100

ОБЪЯСНЕНИЕ ПОРЯДКА ВЫПОЛНЕНИЯ:
1. "static logic" - статический блок выполняется при загрузке класса
2. "logic (1) id= 0" - первый логический блок, id еще не инициализирован (значение по умолчанию 0)
3. "logic (2) id= 1" - второй логический блок, id уже инициализирован значением 1
4. "ctor id= 100" - конструктор, присваивает id новое значение 100
*/