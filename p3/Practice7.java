public class Practice7 {
    public static void main(String[] args) {
        int a = 120;
        
        // byte b = a + 10; 
        // ОШИБКА КОМПИЛЯЦИИ: результат выражения a + 10 имеет тип int (130)
        // Компилятор Java не позволяет неявное сужающее преобразование из int в byte
        // даже если числовое значение теоретически помещается в диапазон byte
        
        byte c = (byte)(a + 10);
        // КОМПИЛИРУЕТСЯ: используется явное приведение типа (byte)
        // a + 10 = 120 + 10 = 130 (превышает максимальное значение byte 127)
        // Происходит переполнение: 130 - 256 = -126
        // Значение переменной c: -126
        
        // byte d = a + 1;
        // ОШИБКА КОМПИЛЯЦИИ: результат выражения a + 1 имеет тип int (121)  
        // Аналогично первой строке - требуется явное приведение типа
        // Несмотря на то, что 121 помещается в диапазон byte (-128 до 127),
        // компилятор требует явного указания приведения
    }
}