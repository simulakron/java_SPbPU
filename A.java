public class A {
    public final int a; // должна быть проинициализирована
    
    // Вариант 1: Инициализация при объявлении
    // public final int a = 10;
    
    // Вариант 2: Инициализация в конструкторе
    public A() {
        this.a = 10;
    }
    
    // Вариант 3: Инициализация в блоке инициализации
    // {
    //     a = 10;
    // }
    
    // Вариант 4: Через параметр конструктора
    public A(int value) {
        this.a = value;
    }
    
    public static void main(String[] args) {
        // Демонстрация всех вариантов инициализации
        
        // Вариант 2: через конструктор по умолчанию
        A obj1 = new A();
        System.out.println("obj1.a = " + obj1.a); // 10
        
        // Вариант 4: через параметр конструктора
        A obj2 = new A(25);
        System.out.println("obj2.a = " + obj2.a); // 25
        
        // Для демонстрации остальных вариантов нужно раскомментировать соответствующий код
    }
}